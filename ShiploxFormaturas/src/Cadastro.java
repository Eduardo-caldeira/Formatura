    private void cadastraAlunoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastraAlunoActionPerformed
        Evento evento = new Evento();
        String nome, cpf,telefone,curso;
        int mesa, idInstituicao, cadastrar=0;
        nome = jTextField4.getText();
        cpf = jTextField5.getText();
        telefone = jTextField6.getText();
        curso = jTextField7.getText();
        idInstituicao = Integer.parseInt(jTextField3.getText());
        mesa = Integer.parseInt(jTextField10.getText());
        
        boolean erro = true;

        for(int i =0; i<adm.getListaEvento().size();i++){
            if(idInstituicao == adm.getListaEvento().get(i).getId()){ //Se existe o evento
                if(verificaMesa(adm.getListaEvento().get(i),mesa)){
                    erro = false; //Se a mesa estiver vazia
                    cadastrar = i; //Salva a posição
                }
            }
        }
        
        if(erro){///se ha erro exibe mensagem de erro
            JOptionPane.showMessageDialog(this,"Dados inválidos\nTente Novamente");
        }else{ ///se não ha, cadastra o aluno
            Aluno aluno = new Aluno(nome,cpf,telefone,curso,mesa);
            adm.getListaEvento().get(cadastrar).getListaAluno().add(aluno);
            jDialog1.setVisible(true);
        }

    }//GEN-LAST:event_cadastraAlunoActionPerformed

