            DefaultTableModel model; //Atualiza a tabela
            model = (DefaultTableModel) tableEvento.getModel();
            while(model.getRowCount()>0){
                model.removeRow(0);
            }
            for(int B=0; B< adm.getListaEvento().size();B++){
                model.addRow(new Object[]{adm.getListaEvento().get(B).getId(),adm.getListaEvento().get(B).getData(),adm.getListaEvento().get(B).getTema(), adm.getListaEvento().get(B).getInstituicao()});
            }
            recarregarTabelaInst();
