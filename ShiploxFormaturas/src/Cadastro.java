    public void lerArquivo(){ //Ler arquivo 
        File file = new File("eventos.txt");
        BufferedReader br;
        
        String end;
        
        List<Evento> listaEvento = new ArrayList<>();
        
        try
        {
            br = new BufferedReader(new FileReader(file));
            
            int nEventos = Integer.parseInt(br.readLine());
                
            for(int i=0; i < nEventos; i++){
                Evento eve = new Evento();
                
                eve.setData(br.readLine());
                eve.setTema(br.readLine());
                eve.setInstituicao(br.readLine());
                eve.setId(Integer.parseInt(br.readLine()));
                
                Vector<Aluno> listaAluno = new Vector<Aluno>();
                
                int nAlunos = Integer.parseInt(br.readLine());
                for(int j=0; j < nAlunos; j++){
                    Aluno al = new Aluno();
                    
                    al.setNome(br.readLine());
                    al.setCpf(br.readLine());
                    al.setTelefone(br.readLine());
                    al.setCurso(br.readLine());
                    al.setMesa(Integer.parseInt(br.readLine()));
                    
                    listaAluno.add(al);
                }
                
                eve.setListaAluno(listaAluno);
                listaEvento.add(eve);
            }
            adm.setListaEvento(listaEvento);

        }
        catch(Exception e)
        {
        }
        //Recarrega a tabela de eventos
        DefaultTableModel model;
        model = (DefaultTableModel) tableEvento.getModel();
        while(model.getRowCount()>0){
            model.removeRow(0);
        }
        for(int i=0; i< adm.getListaEvento().size();i++){
            model.addRow(new Object[]{adm.getListaEvento().get(i).getId(),adm.getListaEvento().get(i).getData() ,adm.getListaEvento().get(i).getTema() , adm.getListaEvento().get(i).getInstituicao()});
        }
        ///FIM DA TABELA DE EVENTOS
        
        recarregarTabelaInst();
    }
    
    public boolean verificaMesa(Evento evento, int mesa){ //Consistência 
        
        for(int i=0;i< evento.getListaAluno().size();i++){
            if(mesa == evento.getListaAluno().get(i).getMesa()){
                JOptionPane.showMessageDialog(this,"Já existe um formando nesta mesa.\n Escolha outra!");
                return false;
            }
        }
        return true;
    }
    
    
